
public class MulTables 
{

	public static void main(String[] args) 
	{
              for(int i=1;i<=30;i++)
              {
            	  System.out.println("Multiplication table of:"+i);
            	  for(int j=0;j<=10;j++)
            	  {
            		  System.out.println(i+"x"+j+"="+(i*j));
            	  }
              }
	}

}
